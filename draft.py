def format_comments(comments, indent=0):
    # 按时间倒序排序评论
    sorted_comments = sorted(comments, key=lambda x: x['create_time'], reverse=True)

    for comment in sorted_comments:
        # 转换时间戳为可读格式
        create_time = datetime.fromtimestamp(comment['create_time']).strftime('%Y-%m-%d %H:%M:%S')

        # 打印评论信息
        print(' ' * indent + f"用户: {comment['user']['nickname']} ({comment['ip_label']})")
        print(' ' * indent + f"时间: {create_time}")
        print(' ' * indent + f"内容: {comment['text']}")
        print(' ' * indent + f"点赞数: {comment['digg_count']}")

        # 如果有回复评论，也按时间倒序排序
        if comment.get('reply_comment') and comment['reply_comment_total'] > 0:
            print(' ' * indent + "回复:")
            sorted_replies = sorted(comment['reply_comment'], key=lambda x: x['create_time'], reverse=True)
            for reply in sorted_replies:
                format_comments([reply], indent + 4)

        print(' ' * indent + "-" * 50)


